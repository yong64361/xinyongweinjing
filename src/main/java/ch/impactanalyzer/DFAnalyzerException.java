package ch.impactanalyzer;

/**
 *
 */
public class DFAnalyzerException extends Exception
{
    public DFAnalyzerException()
    {
    }

    public DFAnalyzerException(String message)
    {
        super(message);
    }

    public DFAnalyzerException(String message, Throwable cause)
    {
        super(message, cause);
    }

    public DFAnalyzerException(Throwable cause)
    {
        super(cause);
    }
}
